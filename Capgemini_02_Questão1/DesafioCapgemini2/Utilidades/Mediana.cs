namespace DesafioCapgemini2
{
    public class Mediana
    {
        /**Para tamanhos impares e numa lista ordenada, a mediana sempre estará na posição 
         *representa a metade do tamnho total da lista 
         */
        public int AcharMediana(int tamanhoLista)
        {
            tamanhoLista = tamanhoLista / 2;
            return tamanhoLista;
        }
    }
}
